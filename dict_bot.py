    # –µ—Å–ª–∏ –ø–µ—Ä–µ–¥ —Å—Ç—Ä–æ–∫–æ–π –ø–æ—Å—Ç–∞–≤–∏—Ç—å –±—É–∫–≤—É f, —Ç–æ –≤ —Ñ–∏–≥—É—Ä–Ω—ã—Ö —Å–∫–æ–±–∫–∞—Ö {} –º–æ–∂–Ω–æ –∏—Å–ø–æ–ª—å–∑–æ–≤–∞—Ç—å –ø–µ—Ä–µ–º–µ–Ω–Ω—ã–µ :)
